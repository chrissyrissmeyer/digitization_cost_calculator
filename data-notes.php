				<article style="width:870px;" class="grid_23 prefix_1 alpha" >

<p style="font-size:14px;">The calculator uses donated data about various aspects of digitization to provide an estimate of possible time and costs for digitization projects. Currently, data in the tool is limited, and not all contributors provide data for each aspect of digitization listed in the tool. Current contributors include:
<ul  style="font-size:14px;">
	<li>Duke University Libraries (2010-2014) -- 18th-20th century materials</li>
	<li>The University of Alabama (2009-2014) -- 19th century materials</li>
	<li>The Triangle Research Libraries Network's consortial grant project "Content Context and Capacity" (CCC) (2011-2013) -- 20th century materials</li>
</ul></p>

<p style="font-size:14px;">Note that while Duke is a member of the Triangle Research Libraries Network and participated in the CCC project, they did not necessarily follow the same workflows for that project that they do normally because CCC was an experiment in largescale digitization. Duke additionally contributed a number of years of data collected from their normal digitization program, and is therefore listed twice as a contributor on some data fields below.
</p>

<p  style="font-size:14px;">
For each piece of information, averages from each institution for which the data is available are averaged together. For example, Duke, Alabama, and TRLN all provided information on the average number of minutes per item spent on quality control of digitized images. The data that you see in the calculator represents these three numbers averaged together.
</p>

